<div class="import-instructions">
    <h3>Formato richiesto per l'importazione CSV</h3>
    
    <p>Il file CSV deve avere le seguenti colonne:</p>
    
    <ul>
        <li><strong>Nome</strong> - Titolo della pagina (obbligatorio)</li>
        <li><strong>Logo</strong> - URL dell'immagine del logo</li>
        <li><strong>Url</strong> - URL della home esterna</li>
        <li><strong>CUP</strong> - Codice Unico di Progetto</li>
        <li><strong>Indirizzo</strong> - Indirizzo fisico</li>
        <li><strong>Contatti</strong> - Informazioni di contatto</li>
        <li><strong>Altre</strong> - Informazioni aggiuntive</li>
    </ul>
    
    <div class="notice notice-important inline">
        <p><strong>Importante:</strong> Il delimitatore del CSV deve essere il punto e virgola (;), non la virgola.</p>
        <p>I ritorni a capo nei campi di testo vengono preservati se racchiusi tra virgolette.</p>
    </div>
    
    <h4>Esempio CSV:</h4>
    <pre>Nome;Logo;Url;CUP;Indirizzo;Contatti;Altre
"Comune di Roma";https://esempio.it/logo1.png;https://comune1.it;"CUP123456789A";"Via del Corso 1, Roma";"Tel: 06 1234567, Email: info@comune.roma.it";"Orari: Lun-Ven 9-18"
"Comune di Milano";https://esempio.it/logo2.png;https://comune2.it;"CUP987654321B";"Piazza Duomo 1, Milano";"Tel: 02 1234567, Email: info@comune.milano.it";"Orari: Lun-Ven 8-17"</pre>
</div>
